package assignments;

public class WhileLoop {

    public static void main(String[] args) {

        int i=2;
        while (i<=100){
            System.out.println("print even numbers= "+i);
            i+=2;
        }


        int j=1;
        while (j<=100){
            System.out.println("print odd number= "+j);
            j+=2;
        }

    }
}
