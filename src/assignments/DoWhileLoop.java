package assignments;

public class DoWhileLoop {
    public static void main(String[] args) {

// print even numbers using do while

        int i=2;
        do {
            System.out.println("print Even numbers= "+i);
            i+=2;
        }while (i<=100);

// print odd numbers using do while

        int j=1;
        do {
            System.out.println("print odd numbes= "+j);
            j+=2;
        }while (j<=100);


    }
}
