package loops_concept;

public class TablesByUsing_Loop {
    public static void main(String[] args) {

        //Table of 2 by using loop

        int number=2;
        for(int i=1 ; i<=10 ; i++)
        System.out.println(number + " * " + i + " = " +(number*i));


        //Table of 5 by using loop

        number=5;
        for(int i=1 ; i<=10 ; i++)
            System.out.println(number + " * " + i + " = " +(number*i));


        //Table of 10 by using loop

        number=10;
        for(int i=1 ; i<=10 ; i++)
            System.out.println(number + " * " + i + " = " +(number*i));


    }
}
