package Loops;

public class Third {
    //print value 25 to 50
    public static void main(String[] args) {
        // using for loop
        for(int i = 25; i<=50; i++){
            System.out.println(i);
        }

        // using while loop
        int j = 25;
        while (j<=50){
            System.out.println(j);
            j++;
        }
    }
}
