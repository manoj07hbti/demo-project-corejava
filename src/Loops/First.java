package Loops;

public class First {
    public static void main(String[] args) {
        // print value 1 to 20

        // Using For loop
        for(int i = 1; i <= 20; i++){
            System.out.println(i);
        }

        // Using While loop
        int j=1;
        while (j<=20){
            System.out.println(j);
            j++;
        }
    }
}
