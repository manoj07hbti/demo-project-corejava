package Loops;

public class Fourth {
    // Print table of 2

    public static void main(String[] args) {

        // Using For Loop
        for(int i = 1; i<=10; i++){
            System.out.println("2 X "+i+" = "+(2*i));
        }

        // Using While Loop
        int j = 1;
        while (j<=10){
            System.out.println("2 X "+j+" = "+(2*j));
            j++;
        }
    }
}
