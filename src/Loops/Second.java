package Loops;

public class Second {
    public static void main(String[] args) {
        //print value 50 to 25

        // using for loop
        for(int i = 50; i>=25; i--){
            System.out.println(i);
        }

        // using while loop
        int j = 50;
        while (j>=25){
            System.out.println(j);
            j--;
        }
    }
}
