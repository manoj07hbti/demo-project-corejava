package loops;

public class for_loop {
    public static void main(String[] args){
        int i;
    for(i=0; i<=100; i++){
        if(i%2==1)

            System.out.println(+i );
        }


    // while loops
        int j=1;
    while(j<=100){
        if(j%2==1)
            System.out.println(+j);
        j++;
    }

    //do while loops
        int k=1;
    do{
        if(k%2==1)
            System.out.println(+k);
        k++;
    }while(k<=100);



        }
    }

