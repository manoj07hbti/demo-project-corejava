package loops;

public class DemoFor {

    public static void main(String[] args) {


        for (int i=0; i<=5; i++){

            System.out.println("hey neeraj...."+ i);

        }
        for (int n=5; n>=0; n--){

            System.out.println("decrement for loop...."+ n);
        }


        for (int v=1; v<=10; ){

            int t=2;
            System.out.println("printing table of 2 : "+ t*v);
            v++;
        }


        for (int k=1; k<=10; ){

            int t=5;
            System.out.println("printing table of 2 : "+ t*k);
            k++;
        }


    }

}
