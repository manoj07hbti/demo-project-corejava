package loops;

public class DOWhileLoop {
    public static void main(String[] args) {
// print value 1 to 20 using increment

        int i=1;
        do {
            System.out.println(i);
            i++;
        }while (i<=20);

// print value 50 to 25  using decrement

        int j=50;
        do {
            System.out.println(j);
            j--;
        }while (j>=25);

// print value 25 to 50 using increment

        int k=25;
        do {
            System.out.println(k);
            k++;
        }while (k<=50);

    }
}
