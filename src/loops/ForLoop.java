package loops;

public class ForLoop {
    public static void main(String[] args) {

// print value 1 to 20 using increment

        for (int i=1; i<=20; i++){
            System.out.println(i);
        }
// print value 50 to 25 using decrement

        for (int j=50; j>=25; j--){
            System.out.println(j);
        }
// print value 25 to 50 using increment

        for (int i=25; i<=50; i++){
            System.out.println(i);
        }

    }
}


