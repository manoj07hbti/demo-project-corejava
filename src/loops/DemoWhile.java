package loops;

public class DemoWhile {

    public static void main(String[] args) {

        int n=1;
        while (n<=5) {
            System.out.println("while loop....." + n);
            n++;
        }

        int v=1;
        while (v>=5){

            System.out.println("decrement of while loops..."+ v);
            v--;
        }

        int k=1;
        int ten=10;
        while (k<=10){

            System.out.println("printing table of ten..."+ ten*k);
            k++;
        }



    }
}
