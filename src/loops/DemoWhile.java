package loops;

public class DemoWhile {

    public static void main(String[] args) {

        int n=1;
        while (n<=5) {
            System.out.println("while loop....." + n);
            n++;
        }

        int v=1;
        while (v>=5){

            System.out.println("decrement of while loops..."+ v);
            v--;
        }

    }
}
