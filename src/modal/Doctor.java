package modal;

public class Doctor {
    String name;
    int exp;
    String speciality;

    public Doctor(String name, int exp, String speciality) {
        this.name = name;
        this.exp = exp;
        this.speciality = speciality;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public String getSpeciality() {
        return speciality;
    }

    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }
}
