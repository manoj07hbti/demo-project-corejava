package test_26_05;

public class EvenAndOdd {
    public static void main(String[] args) {

// Write a code to check even and odd number 3 to 300

        for (int i=3; i<=300; i++){
            if (i%2==0){
                System.out.println("print even numbers= "+i);
            }else{
                System.out.println("print odd numbers= "+i);
            }
        }
    }

}
