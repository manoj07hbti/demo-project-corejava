package test_08_june;

public class EvenOrOdd {

    public static void main(String[] args) {

        //5. Count the Number of Even and Odd Elements
        //Input: [1, 2, 3, 4, 5, 6]
        //Output: Even: 3, Odd: 3

        int EO[]={1,2,3,4,5,6};

        int n=0;
        while (n<1){

            System.out.println("printing even elements...."+EO[1]+","+EO[3]+","+EO[5]);

            n++;
        }

        int k=0;
        while (k<1){

            System.out.println("printing odd elements...."+EO[0]+","+EO[2]+","+EO[4]);

            k++;
        }



    }
}
