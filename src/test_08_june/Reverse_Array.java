package test_08_june;

public class Reverse_Array {
    public static void main(String[] args) {

        //3. Reverse an Array
        //Input: [1, 2, 3, 4, 5]

        int[] array = { 1 , 2 , 3 , 4 , 5 };
        for( int i=4 ; i > -1 ; i-- ){
            System.out.print(array[i]);
        }
    }
}
