package test_08_june;

public class ReverseArray {

    public static void main(String[] args) {



        //3. Reverse an Array
        //Input: [1, 2, 3, 4, 5]
        //Output: [5, 4, 3, 2, 1]

        int rev[]={1,2,3,4,5};

        int n=4;
        do {
            System.out.println("reversing element ....."+rev[n]);
            n--;
        }while (n>=0);




    }

}
