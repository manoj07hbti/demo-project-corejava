public class area_triangle {
    public static void main(String[] args){
        // find the area of triangle with base = 5.6 cm and the height =9.89cm
        float base = 5.6f;
        float height = 9.89f;
        double area = (double) 1/2*base*height;
        System.out.println("area of a tringle is = "+area);
    }
}
