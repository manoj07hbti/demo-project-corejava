package conditional_statements;

public class PuneLocal
{
    public static void main(String[] args)
    {
        String city = "Delhi";
        if (city == "Pune")
        {
            System.out.println("The candidate belongs to Pune...");
        }
        if (city != "Pune")
        {
            System.out.println("The candidate do not belongs to Pune...");
        }
    }
}
