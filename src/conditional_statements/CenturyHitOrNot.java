package conditional_statements;

public class CenturyHitOrNot
{
    public static void main(String[] args)
    {
        int runs = 96;
        if (runs >= 100)
        {
            System.out.println("The batsman hits the century...");
        }
        if (runs < 100)
        {
            System.out.println("The batsman do not hit the century...");
        }
    }
}
