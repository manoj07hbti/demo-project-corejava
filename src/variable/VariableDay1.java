package variable;

public class VariableDay1 {

    public static void main(String[] args) {
        // This code is example of basic variables in Java
        // SYNTAX : Datatype varName;
        // I need to store a name
        String name; // We have created a variable with String datatype
        //store value into variable
        //varName=value;
        name= "Rahul"; // always use double quot while using String value
        //System.out.println("Message : " +varName);
        System.out.println("Printing Name : " +name);

        //I need to store age of a person
        int age; // we have created a variable with int Datatype
        age=23;
        System.out.println("Printing age: " +age);
        //I need to store price of a product
        double price; // we have created a variable with double datatype
        //I need to store section of a Student
        price=243.63;

        System.out.println("Printing price : "+price);
        Character section; // we have created a variable with Character datatype
        section='A'; // Character are always used in single quot
        System.out.println("Printing section: "+section);




    }
}
