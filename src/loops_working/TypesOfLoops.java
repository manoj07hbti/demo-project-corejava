package loops_working;

public class TypesOfLoops {
    public static void main(String[] args) {

        // DO WHILE LOOP

        //do {
        //   CODE
        //   increment/decrement;
        //} while(condition);
        int i=1;
        do{
            System.out.println("Hello World..."+i);
            i++;
        } while(i<=5);

        // INFINITE LOOP

        for(int J = 1; J >=1; J++){

            System.out.println("This is infinite loop: "+ J);
        }

    }
}
